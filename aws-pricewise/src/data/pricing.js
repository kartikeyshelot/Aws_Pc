function expandRDS(compactData) {
  const result = {};
  for (const [region, instances] of Object.entries(compactData)) {
    result[region] = {};
    for (const [type, arr] of Object.entries(instances)) {
      result[region][type] = {
        vcpu: arr[0], memory: arr[1],
        prices: { mysql: arr[2], postgres: arr[3], mariadb: arr[4] },
      };
    }
  }
  return result;
}

function expandAurora(compactData) {
  const result = {};
  for (const [region, data] of Object.entries(compactData)) {
    result[region] = { storagePerGB: data.storagePerGB, ioPerMillion: data.ioPerMillion, instances: {} };
    for (const [type, arr] of Object.entries(data.instances)) {
      result[region].instances[type] = {
        vcpu: arr[0], memory: arr[1],
        prices: { mysql: arr[2], postgres: arr[3] },
      };
    }
  }
  return result;
}

function expandElastiCache(compactData) {
  const result = {};
  for (const [region, nodes] of Object.entries(compactData)) {
    result[region] = {};
    for (const [type, arr] of Object.entries(nodes)) {
      result[region][type] = { vcpu: arr[0], memory: arr[1], price: arr[2] };
    }
  }
  return result;
}

