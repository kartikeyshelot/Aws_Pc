function expandEC2(compactData) {
  const result = {};
  for (const [region, instances] of Object.entries(compactData)) {
    result[region] = {};
    for (const [type, arr] of Object.entries(instances)) {
      result[region][type] = {
        vcpu: arr[0],
        memory: arr[1],
        prices: {
          linux:   arr[2],
          windows: arr[3],
          rhel:    arr[4],
          suse:    arr[5],
        },
      };
    }
  }
  return result;
}

